package Lecture_3_First_JAVA_Program;

import java.util.Scanner;
public class Input_Using_Scanner_Class {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        System.out.println(sc.nextInt());
    }
}
//Scanner Class Provide in java.util.Scanner Packages
//To use Scanner Class(Input) First Import packages(java.util.Scanner)
//Then Create Object Of Scanner Class And Then Use
//Here Scanner is Class
//And sc is Object Of Scanner Class
//Scanner(System.in) is Use To Create Object
//System.in is Define Where To Input Take Command Line Or File (Here System.in Means Input Taken From Command Prompt)